		independence. Challenged Sailors San Diego’s adaptive sailing program is designed to minimize
		these restrictions for our sailors. Learning the skills to maneuver a boat throughout the waters
		of San Diego Bay provides a sense of confidence, autonomy, and accomplishment that can have a
		significant positive effect on many aspects of the sailors’ lives.
	</p>
	<p>
		The volunteers and participants of Challenged Sailors San Diego have experienced the
		transformative nature of adaptive sailing personally. Some are very experienced sailors, some
		are beginners but all have seen a tremendous improvement in their lives through adaptive
		sailing.
	</p>
